def check_storage_health() -> bool:
    """
    Check if storage is accessible and functioning.
    
    Returns:
        True if storage is healthy, False otherwise.
    """
    try:
        from situation_monitor.config.settings import get_settings
        settings = get_settings()
        
        # Check if data directory exists and is writable
        if not settings.data_dir.exists():
            return False
        
        # Try to write a test file
        test_file = settings.data_dir / ".health_check"
        try:
            test_file.touch()
            test_file.unlink()
        except (OSError, PermissionError):
            return False
        
        return True
    except Exception:
        return False
